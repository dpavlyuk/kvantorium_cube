turtle.right(45)
turtle.forward(storona_kletki)
turtle.right(135)
turtle.forward(storona_kletki_diagonal * 3)
turtle.left(135)
turtle.forward(storona_kletki)
turtle.left(45)
turtle.forward(storona_kletki_diagonal * 3)
turtle.right(45)
