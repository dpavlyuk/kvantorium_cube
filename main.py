import turtle

storona_kletki = 100
storona_kletki_diagonal = 50

turtle.penup()
turtle.goto(-200, 100)
turtle.pendown()
turtle.pensize(2)

# Передняя грань
turtle.fillcolor('yellow')
turtle.begin_fill()
turtle.forward(storona_kletki * 3)
turtle.right(90)
turtle.forward(storona_kletki * 3)
turtle.right(90)
turtle.forward(storona_kletki * 3)
turtle.right(90)
turtle.forward(storona_kletki * 3)
turtle.end_fill()
# Клетки передней грани
turtle.right(90)
