turtle.forward(storona_kletki * 3)
turtle.left(90)
turtle.forward(storona_kletki)
turtle.left(90)
turtle.forward(storona_kletki * 3)
turtle.left(90)
turtle.forward(storona_kletki * 2)
turtle.left(90)
turtle.forward(storona_kletki)
turtle.left(90)
turtle.forward(storona_kletki * 3)
turtle.right(90)
turtle.forward(storona_kletki)
turtle.right(90)
turtle.forward(storona_kletki * 3)
turtle.right(90)
turtle.forward(storona_kletki * 2)

# Верхняя грань
turtle.right(45)
turtle.fillcolor('blue')
turtle.begin_fill()
turtle.forward(storona_kletki_diagonal * 3)
